import { execFileSync } from "node:child_process"

const DATASET_ID = "a485ef05-3d6f-4a34-9e6e-90d7d1250d0e"
const DISPLAY_NAME = "outline-content-relevance"
const FEEDBACK_KEY = "outline_content_relevance"
const PROMPT_NAME = "eval_content_writer_agent_outline_content_relevance"
const API_BASE_URL =
	process.env.LANGSMITH_BASE_URL ?? "https://api.smith.langchain.com"

function requireEnv(name: string): string {
	const value = process.env[name]
	if (!value) {
		throw new Error(`${name} is required`)
	}

	return value
}

function runCommand(
	command: string,
	args: string[],
	env?: NodeJS.ProcessEnv,
): string {
	return execFileSync(command, args, {
		encoding: "utf8",
		env: { ...process.env, ...env },
	})
}

function listEvaluators(): Array<{ name?: string; dataset_id?: string }> {
	const output = runCommand("langsmith", ["evaluator", "list", "--format", "json"])
	return JSON.parse(output) as Array<{ name?: string; dataset_id?: string }>
}

function pushPrompt(): string {
	const pythonScript = `
from langsmith import Client
from langchain_core.prompts.structured import StructuredPrompt
from langchain_openai import ChatOpenAI

client = Client()

schema = {
    "title": "OutlineContentRelevance",
    "type": "object",
    "strict": True,
    "properties": {
        "reasoning": {
            "type": "string",
            "description": "Step-by-step reasoning justifying the verdict before deciding true or false."
        },
        "${FEEDBACK_KEY}": {
            "type": "boolean",
            "description": "True when the generated content matches the purpose and intent of each outline section."
        }
    },
    "required": ["reasoning", "${FEEDBACK_KEY}"]
}

system_prompt = """You are grading whether the content generated by a content-writer-agent matches the purpose and intent of the outline provided in the instructions.

Return true only when:
- The actual text generated in each section matches the purpose and intent described in the corresponding outline section.
- Topics, messaging, key points, and information align with what was requested.
- The content addresses the subject matter specified in the outline rather than being generic filler.
- If the outline describes specific points, arguments, or data to include in a section, the generated content covers them.

Return false when:
- The generated content contradicts or ignores the intent of an outline section.
- Content is off-topic, placeholder-like, or generic filler that does not address the outline specifics.
- Key points or topics explicitly mentioned in the outline for a section are absent from the generated content.
- The output introduces content that has no basis in the outline and displaces requested content.

Focus only on whether the content matches the outlined purpose. Do not judge structural ordering, formatting, tone, or style -- only whether the substance of the generated text fulfills the outline intent.

Judge only from the instructions (outline) and the XML diff output."""

human_prompt = """Please grade the following example according to the above criteria.

## Examples

Example 1
Instructions: Create a blog post about AI content optimization. The introduction should explain why AI search engines are changing SEO. The strategies section should cover keyword clustering, entity optimization, and structured data.
Output: The introduction discusses how AI-powered search engines like Perplexity and SearchGPT are reshaping SEO practices. The strategies section covers keyword clustering with examples, entity optimization techniques, and structured data markup.
Verdict: true because each section addresses the specific topics and purposes outlined in the instructions.

Example 2
Instructions: Write a product page for a project management tool. The hero should emphasize speed and simplicity. The features section should highlight real-time collaboration, Gantt charts, and integrations.
Output: The hero section talks about "powerful enterprise-grade solutions for complex workflows" (not speed and simplicity). The features section covers real-time collaboration and Gantt charts but replaces integrations with a generic "and more" bullet.
Verdict: false because the hero content contradicts the outlined purpose (speed/simplicity vs enterprise complexity) and the features section omits a specifically requested topic (integrations).

## Instructions

{instructions}

## Content to review with inline changes

{output}"""

prompt = StructuredPrompt.from_messages_and_schema(
    [
        ("system", system_prompt),
        ("human", human_prompt),
    ],
    schema=schema,
)

model = ChatOpenAI(model="gpt-5.4-mini", temperature=0)
chain = prompt | model

url = client.push_prompt("${PROMPT_NAME}", object=chain, is_public=False)
print(url)
`.trim()

	return runCommand("python3", ["-c", pythonScript], {
		OPENAI_API_KEY: "placeholder",
	}).trim()
}

async function createRule(langsmithApiKey: string) {
	const response = await fetch(`${API_BASE_URL}/api/v1/runs/rules`, {
		method: "POST",
		headers: {
			"x-api-key": langsmithApiKey,
			"Content-Type": "application/json",
		},
		body: JSON.stringify({
			display_name: DISPLAY_NAME,
			dataset_id: DATASET_ID,
			sampling_rate: 1.0,
			filter: "eq(is_root, true)",
			evaluators: [
				{
					structured: {
						hub_ref: `${PROMPT_NAME}:latest`,
						variable_mapping: {
							instructions: "input.messages",
							output: "output.xmlDiffTextContent",
						},
						include_reasoning: true,
						model: {
							lc: 1,
							type: "constructor",
							id: ["langchain", "chat_models", "openai", "ChatOpenAI"],
							kwargs: {
								model: "gpt-5.4-mini",
								temperature: 0,
								openai_api_key: {
									lc: 1,
									type: "secret",
									id: ["OPENAI_API_KEY"],
								},
							},
						},
					},
				},
			],
		}),
	})

	if (!response.ok) {
		throw new Error(await response.text())
	}

	return response.json()
}

async function main() {
	const langsmithApiKey = requireEnv("LANGSMITH_API_KEY")
	const existingEvaluator = listEvaluators().find(
		(evaluator) =>
			evaluator.dataset_id === DATASET_ID && evaluator.name === DISPLAY_NAME,
	)

	if (existingEvaluator) {
		console.log(`Evaluator "${DISPLAY_NAME}" already exists.`)
		return
	}

	const promptUrl = pushPrompt()
	const rule = await createRule(langsmithApiKey)

	console.log(`Prompt pushed: ${promptUrl}`)
	console.log(`Evaluator created: ${DISPLAY_NAME}`)
	console.log(JSON.stringify(rule, null, 2))
}

main().catch((error) => {
	console.error(error)
	process.exit(1)
})
